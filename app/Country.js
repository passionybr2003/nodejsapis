// Function to get country codes by passing country list
export const getcountryCodes = (countryList) => {
  const countryCodes = [];
  if (countryList) {
    countryList.map((country) => {
      if (country.value)
        countryCodes.push(country.value);
    });
  }
  return countryCodes;
};
